<h2><strong>Types of Hajj?</strong></h2>
<p>There are three ways of performing Hajj. You may choose whichever type of Hajj you wish. But the best kind of them is Hajj at-Tamattu.</p>
<p><strong>Hajj al-Ifrad </strong>(single): To perform the Hajj only.</p>
<p><strong>Hajj al-Qiran </strong>(Combined): This means to perform Hajj and Umrah combined or jointly without interruption until one casts the pebbles on the Eid day, and shaves off his head. This kind of Hajj requires Hadee (a sacrificial animal).</p>
<p><strong>Hajj at-Tamattu</strong> (interrupted): Performing the Umrah during the months of Hajj. Once a pilgrimage completes the Umrah, he may take off the Ihram only to re-enter it on the 8<sup>th</sup> of Dhul-Hijjah (known as the Day of Tarwiyah) with the intention of performing the Hajj. The performer of this type of Hajj does not have to go to the Meeqat to re-enter the state of Ihram for performing the Hajj; he is allowed to do so in his residence in Makkah or Mina. This kind of Hajj requires a Hadee (a sacrificial animal) as well.</p>
<h2><strong>Rituals of Hajj</strong></h2>
