<p>Hajj is one of the basic five pillars of our Divine Religion (Islam). It represents one of the best experiences in the life of a Muslim. It is obligatory on every Muslim, man or woman, who is capable of a journey to Makkah once in a lifetime. The pilgrim should perform the rituals and duties of Hajj during the Sacred Month of Dhul-Hijjah, the 12<sup>th</sup> month of Islamic calendar.</p>

