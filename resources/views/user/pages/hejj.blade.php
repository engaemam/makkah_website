<h3><strong><em>Assuming Ihram</em></strong></h3>
<p>Ihram is the state entered into at the Meeqat in which certain acts and types of clothing are forbidden. It is mustahab (desirable) for one who intend to go to Hajj or Umrah to take ghusl (bath) for Ihram. Then the male may wear whatever clothes he wishes that are not made in the shape of the body. (unstitched clothes). The woman is not allowed to remove anything from her clothes that are prescribed for her in the shariah. One may use the body oil and perfume as well. The women should use that which has no odor. All these should be made before making an intention of Ihram at the meeqat.</p>
<h3><strong><em>Ihram and Intention (Niyyah)</em></strong></h3>
<p>It is obligatory for he who comes at the meeqat to assume Ihram and to make the intention of performing the Hajj. He must utter words and actions that are reported in the authentic Ahadith. Recite the Talbiyyah intending to enter into Ihram.</p>
<h3><strong><em>The meeqats</em></strong></h3>
<p>Meeqats (places for assuming Ihram) are five: Dhul Hulaifah, Al-Juhfah, Qarn-ul-Manazil, Yalamlam and Dhatu Irq. These are for those who live there and who pass by them intending Hajj or Umrah. As for those who live in Makkah or near then they are allowed to make Ihram from their houses. The inhabitants of Makkah can make Ihram from Makkah.</p>
<h3><strong><em>Entering the Holy Mosque</em></strong></h3>
<p>While entering the Holy Mosque, one should not forget to enter it by the right foot, and say the supplication.</p>
<h3><strong><em>Tawaf of Qudoom (arrival circumambulation) </em></strong></h3>
<p>One should go straight to Black Stone and facing it make takbeer. One may say Bismillah before it – as it is authentically reported from Ibn Umar. If possible he should touch the Black Stone and kiss it, but if it is not possible then he can make a sign towards it with his hand. It should be done in every single circuit. On arriving between the two corners, he should say this supplication:</p>
<p style="text-align: center;"><span style="color: #339966;">رَبَّنَا آتِنَا فِي الدُّنْيَا حَسَنَةً وَفِي الآخِرَةِ حَسَنَةً وَقِنَا عَذَابَ النَّارِ</span></p>
<h3><strong><em>Praying two units at Maqam-e-Ibrahim</em></strong></h3>
<p>After completing the seven rounds, he should place the Maqam-e-Ibrahim between himself and the Kabah and then pray two units of prayer. He should recite Surat-ul-Kafiroon and Surat Al-Ikhlas in these two units of prayer.</p>
<h3><strong><em>Sai between Safa and Marwah</em></strong></h3>
<p style="text-align: center;">After praying two units of prayer at Maqam-e-Ibrahim, a performer must do sai between Safa and Marwah (two small hills). On reaching Safa hill one should recite:</p>
<p style="text-align: center;"><span style="color: #339966;">إِنَّ الصَّفَا وَالْمَرْوَةَ مِن شَعَآئِرِ اللّهِ أَبْدَأُ بِمَا بَدَأ اللهُ بِهِ.</span></p>
<p>He should go off for seven times.</p>
<h3><strong><em>Calling aloud with Talbiyyah (for Hajj on the Day or Tarwiah)</em></strong></h3>
<p>The Day of Tarwiah is on the 8<sup>th</sup> of Dhul-Hijjah. One should call out the talbiyyah of Hajj. The performer of Hajj should go to Mina and pray Zuhr prayer and remain there, spending the night and praying the rest of the five compulsory prayers. Shortening all prayer into two units but not combining them.</p>
<h3><strong><em>Proceeding to Arafat</em></strong></h3>
<p>One the 9<sup>th</sup> of Dhul-Hijjah, after the sun rises, he should move off towards Arafat reciting the Talbiyyah. There the Imam should give an appropriate Khutbah to the people, lead them in Zuhr and Asr prayers, shortening and combine them in the time of Zuhr with one Adhan and two Iqamah.</p>
<h3><strong><em>Standing in Arafat</em></strong></h3>
<p>Standing in Arafat is one of the important pillars of the Hajj without it the Hajj will not be completed. One should stand there and ask Allah for His mercy. While standing in Arafat one should recite this supplication:</p>
<p style="text-align: center;"><span style="color: #339966;">لا إلَهَ إِلاَّ اللهُ وَحْدَهُ لا شَرِيكَ لَهُ، لَهُ المُلْكُ ؛ وَلَهُ الحَمْدُ ، وَهُوَ عَلَى كُلِّ شَيْءٍ قَدِيرٌ</span></p>
<p>It is not allowed for those who perform Hajj to fast that day. He should remain in such state, remembering his Lord, reciting the Talbiyyah, making supplication as he wishes until the sun sets.</p>
<h3><strong><em>Leaving Arafat</em></strong></h3>
<p>After the sun sets, he should leave Arafat for Muzdalifah, going calmly and with tranquility. After reaching Muzdalifah, he should perform Maghrib and Isha prayers there with one adhan and two Iqamah. The pilgrim is allowed to sleep and rest there until Fajr. After the dawn appears, he should establish Fajr Prayer with Adhan and Iqamah.</p>
<h3><strong><em>At Mahsar Al-Haram</em></strong></h3>
<p>After spending the night in Muzdalifah, he should go to the Mashar Al-Haram (a small mountain in Muzdalifah), climb it, faces the Qiblah and recite (Tahweed, takbeer, tahleel) and declares Allah’s Oneness and supplicate Allah till the brightness of the sky. Then, before the sun rises he should leave for Mina calmly and reciting the Talbiyyah.</p>
<h3><strong><em>The Stoning</em></strong></h3>
<p>The pilgrim must collect or pick stones (pebbles) in Mina, for the intending to do the stoning of Jamrat-ul-Aqabah. He should face the Jamarah (Pillar), having the Makkah on his left and Minah to his right, stone it with seven small pebbles reciting takbeer on each pebble.</p>
<h3><strong><em>The Sacrifice</em></strong></h3>
<p>After casting the pebbles, slaughter your sacrificial animal or have it slaughtered. If one cannot afford to sacrifice an animal, he may fast for three days during the Hajj season, and seven after returning home. After sacrifice, the pilgrim should shorten his hair or shave it off. As for females, it suffices them to gather the end of their hair and clip a piece of it.</p>
<h3><strong><em>Tawaf-ul-Ifadha</em></strong></h3>
<p>After sacrifice, the pilgrim should go to the Sacred House and make tawaf, seven times around. Then he should do sai between Safa and Marwah as before – except for he who does Hajj of Quran and Ifrad – the first Sai being enough for them. On completing tawaf everything will be lawful for him, even the woman (sexual intercourse).</p>
<h3><strong><em>Staying overnight in Mina</em></strong></h3>
<p>The pilgrim should returns to Mina and remain there for the days of Tashreeq. Stone three Jamarat with seven stones in each those days.</p>
<h3><strong><em>The Farewell Tawaf</em></strong></h3>
<p>On deciding to travel back and leaving the Makkah and doing all the rituals, the pilgrim should make farewell tawaf of the Sacred House. On finishing his tawaf and leaving the Holy Mosque, he should not forget to leave by his left foot first and saying the supplication.</p>
